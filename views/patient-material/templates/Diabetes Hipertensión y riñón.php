            <FONT FACE="Arial, sans-serif"><I>)
                </I></FONT>
            <FONT FACE="Arial, sans-serif">y que mide cantidades muy
                pequeñas de albúmina en la orina, este examen es muy importante </FONT>
        </FONT>
