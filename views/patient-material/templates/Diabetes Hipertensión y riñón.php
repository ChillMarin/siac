        </FONT>
        <FONT FACE="Arial, sans-serif">
            <FONT SIZE=2><B>un
                    buen funcionamiento de los riñones es imprescindible para tener una
                    buena salud.</B></FONT>
        </FONT>
    </P>
    <P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
    </P>
    <P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY>
        <FONT FACE="Arial, sans-serif">
            <FONT SIZE=2>Los
                riñones son particularmente susceptibles a dañarse por las
                alteraciones que se producen en los pacientes con hipertensión
                arterial y en los pacientes con diabetes, pero cuando el paciente
                es diabético e hipertenso a la vez este daño se produce mas
                rápidamente y en una forma más severa. En las etapas iniciales del
                daño renal el paciente no nota ningún cambio, y se puede evidenciar
                solamente con exámenes muy sencillos en la sangre y en la orina que
                su médico tratante debe hacerle anualmente.</FONT>
        </FONT>
    </P>
    <P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
    </P>
