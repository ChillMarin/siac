    <P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY>
        <FONT SIZE=2>
            <FONT FACE="Arial, sans-serif">La
                importancia de detectar en forma anticipada el daño renal en un
                paciente diabético e hipertenso radica en que etapas precoces el
                avance del deterioro renal se puede hoy en día prevenir, detener e
                inclusive revertir completamente. Lo primero que hay que tener
                siempre en mente como medida fundamental para prevenir las
                complicaciones de la diabetes y la Hipertensión Arterial es mantener
                en estricto control los niveles de glicemia y de tensión arterial,
            </FONT>
            <FONT FACE="Arial, sans-serif"><B>es decir la glicemia nunca
                    debe estar por enzima de 126 mg/dl. Y la tensión arterial nunca debe
                    estar por enzima de 124/78 mmHg.</B></FONT>
        </FONT>
    </P>
