                precoces de lesión renal, comienzan a detectarse muy pequeñas
                cantidades de albúmina en la orina esto se detecta con un examen muy
                sencillo que se realiza en una muestra normal de orina que se llama
                microalbuminuria ( </FONT>
            <FONT FACE="Arial, sans-serif"><I>micro
