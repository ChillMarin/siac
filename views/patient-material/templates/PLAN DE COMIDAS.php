    <H2 LANG="es-VE" CLASS="western">Tips sobre el Plan de Alimentación</H2>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <P CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Times New Roman, serif">
            <FONT SIZE=2>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE"><B>Para
                                controlar la cantidad de grasas saturadas y el colesterol dietario,
                                usted debe ..</B></SPAN></FONT>
                </FONT>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE">
                        </SPAN></FONT>
                </FONT>
            </FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Comer
                        hasta 6 onzas (cocidas) por día de carne magra, pescado y pollo sin
                        piel.</FONT>
                </FONT>
            </P>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Probar
                        platos principales que contengan pastas, arroz, caraotas y/o
                        vegetales. O cree platos “ bajos en carne”, por medio de la
                        mezcla de estos alimentos con pequeñas cantidades de carne magra,
                        pollo sin piel o pescado.</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="margin-left: 0.41in; line-height: 200%">
        <BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Utilice
                        métodos de cocina que requieran poco o ninguna grasa – hervir,
                        asar, hornear, sancochar, al vapor, salteado, sofrito o en
                        microondas.</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="margin-left: 0.41in; line-height: 200%">
        <BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Use
                        cerca de 5 a 8 porciones en forma de cucharaditas de grasas y
                        aceites diarias, para cocinar y hornear, y en aderezos de ensaladas
                        y pastas para untar.</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="margin-left: 0.41in; line-height: 200%">
        <BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Quite
                        la grasa visible de la carne y el pollo antes de cocinarlos. Drene
                        toda la grasa luego de dorarlos. Enfríe las sopas y estofados
                        después de cocinarlos, para poder remover la grasa endurecida que
                        se encuentra en la parte superior.</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="margin-left: 0.41in; line-height: 200%">
        <BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Limite
                        su ingesta diaria total de colesterol a un promedio de 300 mg. Los
                        huevos y los mariscos pueden ser una fuente importante de colesterol
                        dietario, pero son bastante bajos en grasas saturadas y grasa total.
                        Las claras de los huevos no tienen grasa o colesterol.</FONT>
                </FONT>
            </P>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Limite
                        los órganos animales como el hígado, cerebro, intestinos, riñones,
                        corazón, mollejas de aves o de ternera.</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="margin-left: 0.41in; line-height: 200%">
        <BR>
    </P>
