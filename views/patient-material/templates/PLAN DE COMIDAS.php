    <P LANG="es-VE">
        <FONT SIZE=3><B>¿Cómo puedo usar este plan?</B></FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Este
                folleto lista los grupos básicos de comida. Le suministra el número
                de porciones diarias de cada grupo de comida, tamaño de las
                porciones y selección de alimentos sugeridas. Es importante
                seleccionar una amplia variedad de alimentos bajos en grasas
                saturadas y colesterol, en cada grupo de comida. También se listan
                los postres, meriendas y bebidas.</FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Comer
                el menor número de porciones en cada grupo de comida le suministra
                suficientes proteínas, vitaminas y minerales – nutrientes que su
                cuerpo necesita diariamente. Coma cantidades moderada de alimentos
                provenientes de la carne, pescado, aves, huevos y grupos grasos.
                Usted puede escoger más porciones de comidas de los otros grupos sí
                no necesita perder peso o desea subir de peso.</FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>El
                secreto del éxito está en el balance. Si usted a veces tiene un
                plato alto en grasas para la cena, haga un balance con comidas bajas
                en grasas tales como vegetales al vapor o una fruta como postre. La
                misma idea aplica para las meriendas altas en grasas o saladas. Coma
                pequeñas cantidades, y haga un balance con las comidas bajas en
                grasas, colesterol y sodio, tales como las frutas, vegetales y
                productos lácteos libres de grasa. </FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>El
                American Heart Association sugiere este pan para todos los americanos
                sanos y niños mayores de 2 años. Los niños en fase de crecimiento
                y los adolescentes tienen necesidades especiales. Ellos deben obtener
                suficientes energías (calorías) y nutrientes cada día. Las mujeres
                que están embarazadas o amamantando – o personas con alteraciones
                médicas como la diabetes, resistencia a la insulina u obesidad –
                deben hablar a su proveedor de salud o a su Dietista Registrado, o un
                dietista licenciado, o un nutricionista, acerca de sus necesidades
                dietarias.</FONT>
        </FONT>
    </P>
