    <H2 LANG="es-VE" CLASS="western">Metas del American Heart Association</H2>
    <H1 LANG="es-VE" CLASS="western"><B>Plan de Alimentación</B></H1>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Lo
                que usted come es importante. Puede ayudarlo a prevenir un ataque al
                corazón o un accidente cerebrovascular.</FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Los
                hábitos saludables de alimentación pueden reducir tres de los
                factores de riesgo del ataque al corazón – colesterol alto,
                presión arterial alta, y exceso de peso. Ellos también ayudan a
                disminuir el riesgo de accidentes cerebrovasculares, debido a que la
                enfermedad cardíaca es uno de los factores mayores de riesgo para
                los accidentes cerebrovasculares.</FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Este
                plan de alimentación suministra los últimos consejos de los
                expertos médicos y nutricionistas. Su seguimiento lo ayudará a
                alcanzar y mantener un patrón de alimentación saludable. Los
                beneficios de esto incluyen un peso corporal normal, niveles
                deseables de colesterol y una presión arterial normal.</FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Este
                plan está basado en estos nuevos lineamientos dietarios del American
                Heart Association:</FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Coma
                        una variedad de frutas u vegetales. Escoja 5 o más porciones por
                        día.</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Coma
                        una variedad de productos con granos, incluyendo granos completos.
                        Escoja 6 o más porciones al día.</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Incluya
                        productos lácteos bajos o libres de grasas, pescados, legumbres
                        (caraotas), pollo sin piel y carnes magras.</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Escoja
                        grasas con 2 gramos o menos de grasas saturadas por porción, tales
                        como margarina líquida o de envase, aceite de canola y aceite de
                        oliva.</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Balancee
                        el número de calorías que consume, con el número utiliza cada
                        día. (Para encontrar el número de calorías, multiplique el número
                        de libras que pesa ahora por 15 calorías. Esto representa el número
                        promedio de calorías utilizadas en un día sí usted es
                        moderadamente activo. Sí usted practica muy poco ejercicio,
                        multiplique su peso por 13 y no por 15. Personas menos activas
                        utilizan menos calorías).</FONT>
                </FONT>
            </P>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Mantener
                        un nivel de actividad física que lo mantenga en buenas condiciones,
                        y que este de acuerdo con el número de calorías que usted consume.
                        Camine o realice otras actividades por al menos 30 minutos en la
                        mayoría de los días. Para perder peso, tenga la suficiente
                        actividad para usar más cantidad de calorías que las que consume
                        cada día.</FONT>
                </FONT>
            </P>
    </UL>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Limite
                        la ingesta de comidas altas en calorías, o bajas en nutrición,
                        incluyendo las alimentos como los refrescos y caramelos, que tienen
                        mucha azúcar.</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <UL>
        <LI>
            <P CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Times New Roman, serif">
                    <FONT SIZE=2>
                        <FONT FACE="Courier New, monospace">
                            <FONT SIZE=3><SPAN LANG="es-VE">Limite
                                    las comidas con alto contenido de grasas saturadas, grasas </SPAN></FONT>
                        </FONT>
                        <FONT FACE="Courier New, monospace">
                            <FONT SIZE=3><SPAN LANG="es-VE"><I>trans</I></SPAN></FONT>
                        </FONT>
                        <FONT FACE="Courier New, monospace">
                            <FONT SIZE=3><SPAN LANG="es-VE">
                                    y/o colesterol, tales como los productos lácteos con contenido
                                    total de grasas, carnes grasosas, aceites tropicales, aceites
                                    parcialmente hidrogenados, aceites vegetales y yemas de huevo. Por
                                    el contrario, escoja comidas bajos en grasas saturadas, grasas </SPAN></FONT>
                        </FONT>
                        <FONT FACE="Courier New, monospace">
                            <FONT SIZE=3><SPAN LANG="es-VE"><I>trans</I></SPAN></FONT>
                        </FONT>
                        <FONT FACE="Courier New, monospace">
                            <FONT SIZE=3><SPAN LANG="es-VE">
                                    y colesterol de los primeros cuatro puntos de la página 2. </SPAN></FONT>
                        </FONT>
                    </FONT>
                </FONT>
            </P>
    </UL>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>Ingiera
                        menos de 6 gramos de sal (cloruro de sodio) por día (2.400
                        miligramos de sodio).</FONT>
                </FONT>
            </P>
        <LI>
            <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3>No
                        tome más de un trago de bebida alcohólica al día sí es mujer, y
                        no más de dos al día sí es hombre. (“Un trago” significa que
                        no tenga más de ½ onza de alcohol puro. Ver ejemplos en la página
                        23).</FONT>
                </FONT>
            </P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Esta
                es una guía fácil de seguir para realizar deliciosas comidas – y
                no tiene que abandonar sus comidas favoritas. Cada comida no tiene
                porque llenar todos los lineamientos. Es importante aplicar los
                lineamientos a su patrón general de alimentación, en al menos
                varios de los días.</FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Estos
                lineamientos pueden hacer más, para ayudar a su salud. Ellos reducen
                su riesgo para otras enfermedades crónicas, incluyendo la diabetes
                tipo 2, la osteoporosis (pérdida ósea) y algunas formas de cáncer.</FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
    </P>
    <P CLASS="western" STYLE="line-height: 200%">
