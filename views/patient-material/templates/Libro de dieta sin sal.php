    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3><B>¿QUE
                    ES LA PRESION SANGUINEA ELEVADA?</B></FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western">La presión sanguínea describe la
        presión ejercida por la sangre en las paredes arteriales. El corazón
        crea esta presión cuando bombea la sangre. Una medición de la
        presión arterial es escrita con dos números, tales como 120/80. Se
        lee como “ciento veinte sobre ochenta”.</P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>El
                primer número, o valor superior, representa la presión sistólica –
                la presión sanguínea cuando el corazón bombea la sangre. EL
                segundo número, o valor inferior, representa la presión diastólica
                – la presión en las arterias cuando el corazón está en reposo
                entre los latidos.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Como
                regla general, cualquier presión sanguínea que exceda en más de
                140/90, es considerada como elevada. Y cuanto mayor sea la presión
                por encima de este valor, mayor será el riesgo de accidentes
                cerebrovasculares, ataques cardíacos, insuficiencia renal y otras
                complicaciones cardiovasculares. Es por eso que el tratamiento de la
                presión sanguínea elevada es tan importante.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Se
                desconoce la causa de la presión arterial elevada en el 90% de los
                casos. Al mismo tiempo, se ha demostrado que las comidas con mucha
                sal contribuyen a la presión sanguínea elevada en ciertas personas
                “genéticamente susceptibles”. Ocasionalmente, la presión
                sanguínea elevada es causada por otros problemas médicos, y en ese
                caso, desaparecerá cuando se corrija el problema. </FONT>
        </FONT>
    </P>
    
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3><B>¿HAY
                    ALGUNA CURA?</B></FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western">Desafortunadamente no hay una cura
        para la hipertensión. Pero hay muchas cosas que usted puede hacer
        para ayudar a controlar la presión sanguínea elevada. Usted puede
        mantener un nivel de peso normal, ejercitarse regularmente, y usar
        este recetario para ayudar a reducir la ingesta de sal.
    </P>
    <P LANG="es-VE" CLASS="western">La hipertensión y el colesterol
        sanguíneo elevado, son factores de riesgo para un ataque al corazón.
        Este recetario le mostrará como ayudar a reducir ambos riesgos, por
        la restricción de la ingesta de sal, grasas saturadas y el
        colesterol.</P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Demasiada
                sal en su sistema retiene agua en su cuerpo, y pone una carga extra
                en su corazón y vasos sanguíneos. Es por eso que una dieta baja en
                sal es a veces efectiva en el tratamiento de la presión arterial
                elevada. La ingesta de comidas que contengan potasio o calcio puede
                ayudar a disminuir la presión sanguínea en algunas personas. </FONT>
        </FONT>
    </P>
    <H1 LANG="es-VE" CLASS="western"></H1>
    <H1 LANG="es-VE" CLASS="western">LA CONEXION SAL-SODIO</H1>
    <P LANG="es-VE" CLASS="western">Muchas personas usan las palabras <I>sal</I>
        y <I>sodio</I> como si fuera la misma. No lo son. La sal es realmente
        el cloruro de sodio, un compuesto químico cuya mitad aproximada es
        sodio.</P>
    
    <P LANG="es-VE" CLASS="western">El sodio es un mineral encontrado en
        la naturaleza y en casi todos los alimentos que comemos. Todos
        necesitamos el sodio para vivir, pero esta necesidad puede ser
        satisfecha sin la adición de una pizca de sal a las comidas. Las
        comidas tienen todo lo que necesitamos.
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>El
                Venezolano promedio come más sodio de lo que necesita para una buena
                salud. Una manera de reducir la ingesta de sodio es disminuyendo la
                sal ordinaria de la dieta, aún así usted no tenga problemas de
                salud. Sí usted tiene una presión sanguínea elevada, su médico
                probablemente le aconsejará que lo haga. Es una manera de ayudar a
                reducir a controlar la presión sanguínea, para reducir su riesgo de
                accidente cerebrovascular, ataques cardíacos e insuficiencia renal.</FONT>
        </FONT>
    </P>
    
    
