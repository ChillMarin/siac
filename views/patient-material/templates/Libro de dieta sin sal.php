    <P LANG="es-VE" CLASS="western"><B>MAS IDEAS PARA SUSTITUCIONES</B></P>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Use suero bajo en grasa en lugar
                de crema agria en las recetas de aderezos para las ensaladas.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Espese las sopas, estofados o
                salsas que han sido desgrasadas, con maicena o harina disueltas en
                líquidos fríos, o con vegetales en puré.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Sustituya con vegetales picados,
                el pan usado para rellenar las aves.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Debido a que las recetas
                incluyen más azúcar que la necesaria, usted puede reducir la
                cantidad de azúcar en un cuarto a un tercio.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Reduzca la sal en las recetas de
                hornear que no lleven levadura.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Añada una gota de jugo de limón
                fresco al agua que usted usa para cocinar las pastas, y elimine la
                sal.</P>
        <LI>
            <P LANG="es-VE" CLASS="western">Use una mezcla de harina de
                trigo completa y harina para todo propósito en las recetas que
                requieren harina regular.
            </P>
    </UL>
     
    <UL>
