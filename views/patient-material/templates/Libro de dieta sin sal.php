        aceptable cantidad de aceite o de margarina.</P>
    
    <H1 LANG="es-VE" CLASS="western"></H1>
    <H1 LANG="es-VE" CLASS="western">IDEAS PARA LA PREPARACION DE COMIDAS</H1>
    <P CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Times New Roman, serif">
            <FONT SIZE=2>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE">Ahora
                            usted sabe que clases de comidas son parte de un plan de comida bajo
                            en sal, bajo en grasa modificada, y bajo en colesterol. Eso esta muy
                            bien. NO es suficiente el </SPAN></FONT>
                </FONT>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE"><I>comprar</I></SPAN></FONT>
                </FONT>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE">
                            comidas saludables. Usted también necesita saber como prepararlas,
                            para que </SPAN></FONT>
                </FONT>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE"><I>permanezcan</I></SPAN></FONT>
                </FONT>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE">
                            de esa manera.</SPAN></FONT>
                </FONT>
            </FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western">Todos los métodos de cocinar no son
        creados iguales. Algunos son mejores que otros, por retener las
        vitaminas y minerales, y por disminuir el colesterol, loas grasas y
        las calorías. Básicamente, aléjese de las técnicas de cocinar que
        añadan grasas, o que permita cocinar en la propia grasa, tales como
        la fritura en grasa o la cacerola.</P>
     
    <P LANG="es-VE" CLASS="western">En cambio, busque las técnicas que
        aumenten el sabor, preserve los nutrientes básicos y mantenga a un
        mínimo, la adición de grasas. Para comenzar, aquí hay unos pocos
        ejemplos de métodos de cocinar que lo ayudarán a su corazón.
    </P>
     
    <P LANG="es-VE" CLASS="western"><I><B>Asar. </B></I>Siempre coloque
        una rejilla en el fondo de la cacerola de asar, para que la carne o
        el pollo no se asienten en sus propios jugos. Asegúrese de asar a
        una temperatura baja, cerca de 350 °F, para evitar secar la carne y
        sellar la grasa. Para asar añadiendo sus propios jugos, use líquidos
        bajos en sodio y libre de grasa, tales como el vino de mesa, el jugo
        de tomate sin sal, o jugo fresco de limón.</P>
     
    <P LANG="es-VE" CLASS="western"><I><B>Hornear. </B></I>El pollo, el
        pescado, y la carne pueden ser horneados en utensilios cubiertos, con
        una pequeña cantidad adicional de líquidos. La humedad que aporta
        el líquido, lo hace particularmente bueno para el pescado y las
        pechugas de pollo, las cuales tienen tendencia a ser un poco secas.</P>
     
    <P LANG="es-VE" CLASS="western"><I><B>Cocinar a fuego lento o
                estofados.</B></I> Este método usa un poco más de líquido que el
        horneado. Inténtelo en un recipiente cubierto en la parte superior
        de la estufa (usando fuego bajo), o en el horno (usando temperaturas
        medias de 300 °– 325 °F.) Sí usted está cocinando a fuego
        lento, o preparando un estofado de carne o pollo, comience el día
        anterior, y refrigere el plato durante la noche. Al día siguiente,
        cuando la grasa fría esté cuajada, usted puede removerla fácilmente
        antes de recalentarla. Cocinar a fuego lento es también una
        excelente manera de cocinar vegetales.</P>
     
    <P LANG="es-VE" CLASS="western"><I><B>Escalfar. </B></I>Para escalfar
        un pollo o un pescado, sumérjalo en una cacerola con líquido
        caliente, en la parte alta de la estufa. Este método funciona
        especialmente bien, cuando usted se sirve la comida con una salsa de
        vegetales y hierbas en puré.
    </P>
     
    <P LANG="es-VE" CLASS="western"><I><B>Asar. </B></I>Colocar la carne
        en una rejilla y cocinar con este método permite que la grasa
        chorree de la carne o del pollo. También es una manera sabrosa de
        filetes de pescado o pescados completos. Para un sabor extra, trate
        de macerar la comida poniéndola sobre los carbones o bajo la
        parrilla. Los vegetales colocados en pinchos también tienen un gran
        sabor cuando se doran directamente sobre la llama.</P>
     
    <P LANG="es-VE" CLASS="western"><I><B>Saltear.</B></I> El pescado, el
        pollo y los vegetales pueden ser salteados en una cacerola abierta
        con poca o ninguna grasa; la alta temperatura y el movimiento
        previenen que se pegue la comida, Trate de saltear con una pequeña
        cantidad con una pequeña cantidad de aceites no saturados frotados
        con un papel absorbente en una cacerola. Mejor todavía, use un
        rociador de aceite vegetal o saltee en una pequeña cantidad de caldo
        bajo en sodio o vino de mesa.</P>
     
    <P LANG="es-VE" CLASS="western"><I><B>Al vapor.</B></I> Cocinar en
        una cesta sobre agua hirviendo a fuego lento, retiene el sabor y
        color natural, y los valores nutricionales de los vegetales intactos.
        Trate de añadir hierbas al agua hirviendo o usando un caldo bajo en
        sodio en vez de agua, para añadir más sabor al plato terminado.</P>
     
    <P LANG="es-VE" CLASS="western"><I><B>Freír agitando. </B></I>Se
        hace en un wok chino, este método se basa en el mismo principio del
        salteo. La alta temperatura y el constante movimiento de la comida
        previene que se peque y se queme. Trate de freír agitando los
        vegetales y pollo en pedacitos , o la comida de mar con una pequeña
        cantidad de aceite de maní. Cuando la receta lo pida, use la soya
        baja en sodio.</P>
     
    <P LANG="es-VE" CLASS="western"><I><B>Cocinar con microondas.</B></I>
        Este es un método rápido y fácil que no requiere añadir grasa.
        Usted no tiene que añadir grasa para prevenir que se pegue la comida
        a la cacerola, debido a que las comidas no tienden a pegarse con el
        calor húmedo del horno de microondas. De hecho, usted puede drenar
        la grasa de la comida, a medida que se cocina, colocándola entre dos
        papeles absorbentes en el microondas.</P>
     
    <P LANG="es-VE" CLASS="western">Sí usted desea adaptar una receta al
        microondas, trate de disminuir el tiempo de cocción en un cuarto a
        un tercio del tiempo convencional. Sí la comida necesita más
        cocción, aumente un poco el tiempo. Usted también puede buscar una
        receta de microondas, similar a la que usted está tratando de
        adaptar. Mantenga en mente lo siguiente cuando cocine en microondas.</P>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Escoja las comidas que se
                cocinan bien en calor húmedo: pollo, pescado, carne molida,
                vegetales, salsas y sopas.</P>
        <LI>
            <P LANG="es-VE" CLASS="western">Pedazos que son aproximadamente
                iguales en tamaño y forma se cocinarán uniformemente.</P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="margin-left: 0.31in"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Usted puede reducir el líquido
                usado en la cocción de bebidas, sopas, vegetales, frutas y platos
                principales en cerca de un tercio, debido a que cuando se cocina con
                microondas, hay menos evaporación.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Escoja un recipiente seguro en
                el microondas, ligeramente más grande que el plato requerido para
                la cocción de la receta en un horno convencional.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Use un nivel alto (100 por
                ciento de poder) para las sopas, bebidas, frutas, vegetales, carne
                molida y pollo. Use un nivel medio-alto (70 por ciento del poder)
                para los estofados hirviendo lentamente. Y use un nivel medio (50
                por ciento del poder) para hornear panes, tortas y muffin, y para
                cocinar cortes de carnes menos blandas.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Para crear un apariencia
                crocante en los productos horneados, engrase los recipientes con un
                aceite vegetal aceptable y añada nueces molidas o pan rallado.
                (Este seguro de calcular los valores para las grasas y/o sodio en
                estas nueces añadidas, cuando decida como se ajusta estas comidas
                en sus planes dietarios).</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Añada quesos bajos en grasa y
                sodio, y otros toppings hacia al final de la cocción, para evitar
                que se pongan duros o aguados.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">No trate de cubrir las carnes
                con harina si va a añadir líquidos para cocinar. La cubierta solo
                se aguará.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Use arroz de cocción rápida en
                vez de grano largo.</P>
    </UL>
    <P LANG="es-VE" CLASS="western" STYLE="margin-left: 0.31in"><BR>
    </P>
     
     
    <P LANG="es-VE" CLASS="western"><B>TIPS PARA COCINAR</B></P>
    <P LANG="es-VE" CLASS="western">Mantenga el sabor, pero disminuya la
        grasa y la sal. Ese es el reto que usted encontrará para una plan de
        comidas baja en sodio y grasa modificada, bajo colesterol. Aquí hay
        unas cuantas claves.</P>
     
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Use un recipiente no adherente,
                de manera que usted pueda cocinar con un mínimo de aceite, o con
                rociador de aceite vegetal, la cual es otra buena técnica para
                cortar la grasa.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Quite toda la grasa visible de
                la carne antes de cocinar.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Después de asar la carne o el
                pollo, enfríe el líquido chorreado en el refrigerador. Una vez que
                esté frío, la grasa se elevará y endurecerá en el tope; de esa
                manera puede ser removida fácilmente y el caldo puede usarse en
                estofados, salsas y sopas.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Compre solo la carne, cochino o
                pavo molida más magra (no mayor de 15 por ciento de grasa). Después
                de dorarla, ponga la carne molida en un colador, cubierto
                interiormente con papel absorbente. Permita que la grasa se drene.
                La “carne molida” tiene generalmente un contenido en grasa más
                alto que la carne sin moler. Aún la carne molida más magra,
                contiene más grasa que la recomendada para aquellos en la dieta del
                Paso Dos (vea las páginas 339-361). En lugar de comprar la carne
                molida, pídale a su carnicero que le muela un bistec de lomito.
                Asegúrese que quite toda la grasa visible de previas moliendas.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Cuando esté calculando los
                tamaños de las porciones, recuerde que la carne pierde cerca del 25
                por ciento de su peso durante la cocción. (Por ejemplo, 4 onzas de
                carne cruda terminarán siendo 3 onzas de carne cocida.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Para hacer salsas sin grasa,
                mezcle una cucharadita de maicena a 1 taza de caldo bajo en sodio a
                temperatura ambiente, batiendo ambos en una jarra con una tapa bien
                ajustada. Luego caliente el resto del caldo sin grasa en un
                recipiente y añada el líquido mezclado. Hierva lentamente hasta
                que se espese.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Hágase el hábito de quitarle
                la piel a los pollos antes de cocinarlo, y quítele toda la grasa
                visible debajo de la piel. La piel es más fácil de quitar sí se
                usan papeles absorbentes o un trapo limpio para fijarlo. Asegúrese
                de limpiar bien la superficie de trabajo y los utensilios con agua
                jabonosa caliente después de preparar el pollo para cocinar.
            </P>
        <LI>
            <P LANG="es-VE" CLASS="western">El pescado fresco debe ser
                cocinado por 10 minutos por pulgada de espesor. Añada 5 minutos sí
                se encuentra envuelto en papel de aluminio. El pescado congelado
                requiere 20 minutos por pulgada de espesor, además de 10 minutos sí
                se encuentra envuelto en papel de aluminio. El tiempo de cocción
                puede variar, dependiendo del método de cocción usado, pero el
                pescado está hecho cuando está opaco y se desmenuza fácilmente.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Prepare los huevos revueltos o
                tortillas, de manera que se use una yema de huevo por porción.
                Añada unas claras de huevo extras al recipiente de mezclar para
                hacer porciones más generosas.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Preserve los jugos naturales en
                las comidas por medio de la envoltura en papel de aluminio antes de
                cocinarlas. O trate de envolver las comidas en bolsas de comidas
                hechas de hojas de lechugas y de repollo grandes preparadas al
                vapor, y colóquelos en el lado de la costura en el plato de hornear
                antes de cocinarlos.</P>
        <LI>
            <P LANG="es-VE" CLASS="western">Cocine los vegetales solo lo
                necesario para que queden tiernos y crocantes. Los vegetales
                sobre-cocinados pierden tanto el sabor como importantes nutrientes.</P>
        <LI>
            <P LANG="es-VE" CLASS="western">Limpie los hongos a medida que
                los use lavándolos con un trapo humedecido. También sirve una
                mojada rápida en agua fría, pero nunca los inmersa porque saldrán
                aguados.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Asegúrese de usar guantes de
                goma cuando manipule los pimientos picantes, o lávese las manos
                cuidadosamente después de la manipulación. La piel, especialmente
                alrededor de los ojos es muy sensible al aceite de os pimientos.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Disminuya el colesterol usando
                más vegetales y menos pollo o carnes en las sopas, estofados y
                cacerolas. También, los vegetales finamente cortados son una gran
                manera de abultar la carne o pollo molido.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Disminuya la grasa en los
                aderezos de ensaladas cremosas, mezclándolos con yogur simple, bajo
                en grasa o sin grasa.</P>
        <LI>
            <P LANG="es-VE" CLASS="western">En vez de comprar un yogur de
                frutas preparado, endulce un yogur simple, bajo en grasa o sin
                grasa, con frutas en puré.</P>
    </UL>
     
     
    <P LANG="es-VE" CLASS="western"><B>AUMENTANDO LOS SABORES SIN EL USO
            DE SAL</B></P>
    <P LANG="es-VE" CLASS="western">Darle sabor a la cocina baja en sal
        es fácil si usted conoce algunos pocos trucos. Las siguientes ideas
        lo ayudarán a aumentar el sabor de una variedad de comidas sin el
        uso de la sal. Después de tratar alguna de estas técnicas, usted
        estará de acuerdo en que las mejores cosas en la vida están libres
        de sal.</P>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Para remover los aceites y
                líquidos salados, escurra el salmón, atún o sardinas enlatados.
                Luego añada agua a la lata y drénela de nuevo para enjuagarla.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Tueste las semillas, nueces y
                especias completas para exponer el sabor completo. Cocínelos en una
                sartén seca en fuego moderado, o en una hoja de hornear en un horno
                a 400 °F.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Ase los vegetales en un horno
                caliente, caramelizará sus azucares naturales.</P>
        <LI>
            <P LANG="es-VE" CLASS="western">Machaque el ajo, chile, y las
                hierbas frescas y especias con un mortero y tritúrelo para que
                liberen sus sabores.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Use el gusto del limón. EL
                sabor del limón está en la parte de la cáscara sin la parte
                blanquecina; mantiene el verdadero sabor de la fruta. Puede rallarla
                con un rallador plano, tipo lámina, o removerla con un pelador de
                vegetales, y luego cortarla el pedazo en tiras.
            </P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Ralle el jengibre fresco con
                rallador plano, tipo lámina. Use un procesador de comidas para
                rallar el rábano fresco, el cual tiene más sabor que el tipo
                salado embotellado.
            </P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Cuando le sea posible, use
                hierbas frescas en vez de las secas. Píquelas y añádalas en el
                último momento, para un sabor más fresco, más “vivo”.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Añada hierbas secas como el
                tomillo, romero y mayorana, para un sabor más picante, pero úselo
                esparcidamente.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Añada vinagre o jugo de frutas
                para un aumento maravilloso del sabor, pero añádalo en el último
                momento. El vinagre es maravilloso en vegetales, tales como los
                verdes, cítricos en frutas como el melón. Cualquiera de los dos es
                grandioso con el pescado.
            </P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Use la mostaza seca o la mostaza
                libre de sal. Mezcle la mostaza seca con agua para hacer un
                condimento fuerte. Usted puede también encontrar la mostaza libre
                de sal embotellada, que es como la mostaza en polvo, como la que
                contiene sal.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Para un poco más de “picada”,
                añada pimientos picantes frescos a sus platos. Quítele la membrana
                y las semillas antes de cortarlos finamente. Ellos están bien
                crudos, y una cantidad pequeña es suficiente.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Los vegetales y las frutas son
                fáciles de aderezar sin sal. Llene un mezclador de hierbas con una
                combinación de hierbas frescas y especias. Uselos en lugar del
                salero. Uno de nuestros favoritos pueden encontrarse en las páginas
                38-39.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Algunos vegetales y frutas como
                el hongo, tomates, chiles, cerezas, arándanos y grosellas le dan un
                sabor más intenso cuando están secas que cuando son frescas. Y si
                éstas son inmersas en agua y reconstituidas, usted logra como un
                bono, el “caldo” natural para utilizarlo posteriormente.</P>
    </UL>
     
    <UL>
        <LI>
            <P LANG="es-VE" CLASS="western">Compre las mejores y más
                frescas de las especias completas y muélalas en in moledor de
                especias. Usted notará una gran diferencia.</P>
    </UL>
     
     
     
    <P LANG="es-VE" CLASS="western"><B>SUSTITUCIONES DE INGREDIENTES</B></P>
    <P LANG="es-VE" CLASS="western">No hay necesidad de tirar las viejas
        recetas familiares y los deleites de las fiestas, porque usted está
        cuidándose en su ingesta de sal. Usted puede mantener muchas de sus
        recetas favoritas, sí está dispuesto a hacer algunas simples
        sustituciones. Solo haciendo algunos cambios, usted reducirá lo
        suficiente la cantidad de sal y grasa, para hacer que casi cualquier
        receta se ajuste perfectamente en su nuevo plan de alimentación.</P>
