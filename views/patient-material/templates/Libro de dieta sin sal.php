                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE">significa
                            que el producto está hecho sin la sal que es usualmente usada en el
                            procesamiento, pero que el producto contiene sodio como una parte
                            natural de la comida.</SPAN></FONT>
                </FONT>
            </FONT>
        </FONT>
    </P>
    
    <H1 LANG="es-VE" CLASS="western"></H1>
    <H1 LANG="es-VE" CLASS="western">ABAJO CON EL COLESTEROL</H1>
    <H2 LANG="es-VE" CLASS="western">Cuando usted vea la frase <I>no
            colesterol</I> en una etiqueta, esto significa que el producto no
        contiene colesterol. Pero puede todavía contener algo de grasa
        saturada, lo cual aumenta el colesterol. Básicamente, cualquier
        producto que no contenga ingredientes de origen animal, está libre
        de colesterol de manera natural. Pero el aceite de coco, palma y de
        semilla de palma – los aceites tropicales – son tres fuentes
        vegetales peligrosa porque contienen grasas saturadas.</H2>
    <H2 LANG="es-VE" CLASS="western"></H2>
    <H2 LANG="es-VE" CLASS="western">Unas pocas etiquetas de productos le
        dará el porcentaje de calorías derivado de la grasa. Las etiquetas
        de la mantequilla de maní y de la margarina a veces dan esta
        información, alertándolo sobre el hecho de que estos productos
        contienen primariamente grasas.</H2>
    <H2 LANG="es-VE" CLASS="western"></H2>
    <H2 LANG="es-VE" CLASS="western"></H2>
    <H2 LANG="es-VE" CLASS="western"><B>CONTANDO LAS CALORIAS</B></H2>
    <H2 LANG="es-VE" CLASS="western">Las palabras <I>bajo en calorías </I>
        significan que el producto no contiene más de 40 calorías por
        porción. <I>Reducido en calorías </I>significa que el producto
        tiene al menos, una tercera parte de las calorías que el producto
        que va a ser sustituido.
    </H2>
    <H2 LANG="es-VE" CLASS="western"></H2>
    <H2 LANG="es-VE" CLASS="western">Cuando usted compre, esté seguro de
        comparar las etiquetas. Algunas comidas premezcladas, congeladas, o
        preparadas, tiene un menor contenido de sodio, ácidos grasos
        saturados o colesterol que otras. Ahora que muchos productos listan
        el contenido de sodio, grasa y colesterol, conseguir comidas bajas en
        sodio, ácidos grasos saturados y colesterol es más fácil que
        nunca.
    </H2>
    <H2 LANG="es-VE" CLASS="western"></H2>
    <H2 LANG="es-VE" CLASS="western"></H2>
    <H2 LANG="es-VE" CLASS="western"><B>COMO LEER LAS ETIQUETAS</B></H2>
    <H2 LANG="es-VE" CLASS="western">Esto es importante. Primero, busque
        los ingredientes listados. Todas las etiquetas de los comestibles,
        colocan los ingredientes de los productos ordenados por peso. El
        ingrediente presente en la mayor cantidad, es colocado primero. El
        ingrediente presente en la menor cantidad es colocado de último.
    </H2>
    <H2 LANG="es-VE" CLASS="western"></H2>
    <H2 LANG="es-VE" CLASS="western">Como una regla practica, sí la sal
        y varios componentes de sodio son colocados como ingredientes, el
        producto contiene más sal que la aconsejable, para una dieta baja en
        sodio. Para evitar mucha grasa total o ácidos grasos saturados,
        limite el uso de productos que coloquen las grasas o aceites de
        primero, o que contengan muchos ingredientes con grasa o aceites.</H2>
    <H2 LANG="es-VE" CLASS="western"></H2>
    <H2 LANG="es-VE" CLASS="western">La etiqueta de nutrición también
        nos dice que tipos de nutrientes suministra el producto, las
        proporciones relativas de estos nutrientes y sus contribuciones a las
        Cantidades Diarias Recomendadas para las proteínas, vitaminas y
        minerales.</H2>
