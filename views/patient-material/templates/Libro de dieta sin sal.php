    <H1 LANG="es-VE" CLASS="western">HUEVOS</H1>
    <P LANG="es-VE" CLASS="western">Una yema de un huevo grande contiene
        casi toda la cantidad permitida diaria de colesterol, aproximadamente
        213 a 220 miligramos. Es una buena idea limitar el consumo de yemas
        de huevo a tres por semana.</P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Por
                el otro lado, las claras de huevo no contienen colesterol, y son una
                excelente fuente de proteínas. Usted puede usar las claras de los
                huevos en vez de los huevos completos. De hecho, en la mayoría de
                las recetas, dos claras de huevo y un poco de aceite aceptable, son
                un excelente sustituto para un huevo completo. (Ver nuestra receta
                para el Sustituto del Huevo sin Colesterol en la página 164) </FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Asegúrese
                de comer sólo huevos, y claras de huevo cocinados, nunca crudos.</FONT>
        </FONT>
    </P>
    
    <H1 LANG="es-VE" CLASS="western"></H1>
    <H1 LANG="es-VE" CLASS="western">PRODUCTOS DE CARNE</H1>
    <P LANG="es-VE" CLASS="western">Una dieta balanceada requiere cierta
        cantidad de comidas con proteínas cada día. Con las carnes,
        mantenga a un mínimo la ingesta de sodio, comprando sólo carne
        magra fresca sin sal, o carne congelada. Y debido a que la carne
        contiene colesterol y grasas saturadas, usted debería limitarse a 6
        onzas diarias.
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Las
                mejores carnes para usted son los cortes magros, con la menor
                cantidad de grasa hacia afuera, y menos grasa marmolada a lo largo
                del corte. Alguno de los mejores cortes son el muchacho redondo y el
                lomito, al igual que la carne molida extra magra. Todos los cortes de
                la ternera son magros excepto las chuletas. Busque el cochino magro,
                tal como el jamón fresco bajo en sodio tomado de la parte central,
                el lomo y la chuleta de lomo.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western">Debe prestarle una atención
        particular a la carne molida. Es mejor seleccionar un corte de carne
        y pedirle al carnicero que le quite la grasa, para luego molerla.
        Usualmente esto es hecho sin recargo adicional.</P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Los
                trozos de pollo cocinados sin piel, es una excelente fuente de
                proteínas. Cuando use el pollo completo y pavo, hornéelo con la
                piel. Esto ayudará a que la carne no se seque. Asegúrese de remover
                la piel antes de comer. Permanezca alejado de los pavos que son
                horneados con el añadido de grasa derretida, porque las grasas
                usadas comercialmente, son altamente saturadas. Aún cuando el pavo
                sea bañado en un caldo, el caldo es usualmente alto en sodio. Es
                mejor que usted mismo realice este proceso con un caldo sin sal.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>En
                general, permanezca lejos de las siguientes carnes que contienen
                grandes cantidades de uno o más de estas cosas que estamos tratando
                de limitar (sodio, grasa y colesterol): las carnes de las cantinas,
                perros calientes, salchichas, costillitas, cochino salado, hígado y
                otras carnes de órganos, carnes curadas, ahumadas o secas, tales
                como el jamón o la tocineta, y carnes o aves enlatados, a menos que
                sean empacados sin sal. </FONT>
        </FONT>
    </P>
    
    <H1 LANG="es-VE" CLASS="western"></H1>
    <H1 LANG="es-VE" CLASS="western">PESCADOS</H1>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>El
                pescado es un favorito bajo en sal versátil. A pesar que no es bajo
                en colesterol, el pescado generalmente contiene menos que las carnes
                rojas. Para una dieta reductora de colesterol, el pescado tiene una
                discreta ventaja sobre la carne roja magra, y una definitiva ventaja
                sobre las carnes rojas con grasa. Por esta razón, muchas autoridades
                recomiendan comer pescado dos o tres veces a la semana.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>El
                pescado por sí mismo, puede ser grasoso o magro. Los ácidos grasos
                3-Omega, encontrados en algunas variedades de pescados grasosos están
                en la actualidad siendo estudiados por los potenciales beneficios
                reductores de los lípidos (grasas de la sangre). Algunos pescados
                con alto contenido de ácidos grasos 3-Omega son: el salmón del
                Atlántico, el bonito, caballas, carpa, pescado blanco de lago,
                salmón y trucha de lago.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>El
                camarón, la langosta, el cangrejo y la mayoría de mariscos son muy
                bajos en grasas. Sin embargo, onza por onza, algunas variedades
                contienen más sodio y colesterol que las aves, carne o pescado. Aún
                estos, sin embargo, pueden ser comidos ocasionalmente dentro de los
                lineamientos recomendados de 300 miligramos de colesterol diarios.</FONT>
        </FONT>
    </P>
    
    <H1 LANG="es-VE" CLASS="western"></H1>
    <H1 LANG="es-VE" CLASS="western">FRUTAS, VEGETALES, GRANOS Y
        LEGUMBRES</H1>
    <P LANG="es-VE" CLASS="western">Con pocas excepciones, las comidas
        frescas en estas categorías no tienen colesterol, tienen tendencia a
        ser bajas en sodio y grasa, y en muchos casos, altos en fibras y
        vitaminas.</P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Las
                excepciones incluyen la carne de los cocos, los aguacates y las
                aceitunas. La carne de coco es alta en ácidos grasos saturados.
                Tanto los aguacates como las aceitunas son altas en grasas, aunque la
                grasas son no saturadas. Las aceitunas verdes son altas en sodio (800
                miligramos en 10 pequeñas), las aceitunas maduras son moderadamente
                altas en sodio (250 miligramos en 5 extra grandes). Manténgase
                alejado de los vegetales que han sido empacados en salmuera, tales
                como los encurtidos y col agria, porque están cargadas de sodio.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>En
                esta categoría, siempre lea las etiquetas e las comidas procesadas.
                Muchas de estas contienen sal añadida, mantequilla o salsas.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>También
                note que las frutas frescas o enlatadas en agua son bajas en
                calorías, que las frutas enlatadas en jugo o jarabe.</FONT>
        </FONT>
    </P>
    
    <H1 LANG="es-VE" CLASS="western"></H1>
    <H1 LANG="es-VE" CLASS="western">NUECES Y SEMILLAS</H1>
    <P LANG="es-VE" CLASS="western">Estos son pasapalos sabrosos, y la
        mayoría están disponibles en sus estados naturales: sin sal. Pero
        recuerde que aunque la grasa en las nueces y las semillas es
        mayormente no saturada, hay mucha de ella, por lo que la cuenta
        calórica es mayor que lo que la gente se imagina. La nueces y las
        semillas son buenas fuentes de proteínas incompletas y pueden
        reemplazar hasta cierto punto, otras fuentes altas en proteínas –
        especialmente cuando se comen con caraotas, legumbres y granos, los
        cuales también son proteínas incompletas. Juntas, ellas suministran
        proteínas completas.</P>
    
    <H1 LANG="es-VE" CLASS="western"></H1>
    <H1 LANG="es-VE" CLASS="western">PANES, CEREALES Y PASTA</H1>
    <P LANG="es-VE" CLASS="western">Los granos completos o los panes
        enriquecidos, los cereales y pastas son una parte importante de una
        dieta balanceada. Ellos proveen muchos nutrientes y relativamente
        pocas calorías. (Pero evite las salsas altas en grasa o en sal, y
        los condimentos que a veces los acompañan).</P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Proceda
                y experimente con diferentes clases de panes, tales como el de trigo
                completo o cuarteado, centeno, francés, italiano y pan moreno. Ellos
                contienen algo de sodio y una pequeña cantidad de grasa, pero no lo
                suficiente para sobreponer a sus bondades naturales.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Vigile
                el sodio, las grasas y los aceites en las etiquetas de las galletas.
                Busque galletas de soda sin sal, o pruebe los matzos, tostadas melba
                o grisines, si quiere algo crujiente. Las galletas de centeno tipo
                escandinavas, y otras galletas de granos completos son a veces hechos
                sin grasas y aceites, y con poca o ninguna sal. También busque
                cotufas, pretzels y grisines sin sal.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Tenga
                cuidado con los productos horneados comercialmente, y mezclas como
