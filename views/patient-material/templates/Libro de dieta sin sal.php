                cereales calientes, el arroz y las pastas casi no contienen sodio –
                solo recuerde de no colocar sal en el agua, cuando prepare estas
                comidas.</FONT>
        </FONT>
    </P>
    
    <H1 LANG="es-VE" CLASS="western"></H1>
    <H1 LANG="es-VE" CLASS="western">GRASAS Y ACEITES</H1>
    <P LANG="es-VE" CLASS="western">Las grasas polinsaturadas y
        monosaturadas y aceites son las clases de grasas que usted desea
        incluir en su dieta diaria. De hecho, 5 a 8 cucharaditas de estas
        grasas y aceites es una cantidad razonable.</P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Usted
                puede usarlas en la forma de aderezos de ensalada sin sal, margarinas
                sin sal o aceites usados en la cocina. Los aceites están libres de
                colesterol y no contienen sodio. La mayonesa y la mayoría de las
                margarinas, por otro lado, contienen algo de sal, pero están
                permitidas de usarse ocasionalmente. La mayoría de los aderezos
                comerciales para ensaladas contienen grandes cantidades de sal y
                deben ser evitadas.</FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3><B>¿QUE
                    TIPO DE GRASA?</B></FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western">Cuando lea las etiquetas en los
        productos empacados, preste atención particular a los tipos de
        grasas y aceites presentes.</P>
    
    <P CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Times New Roman, serif">
            <FONT SIZE=2>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE"><I><B>Grasas
                                    Saturadas</B></I></SPAN></FONT>
                </FONT>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE">.
                            Las grasas y aceites altas en ácidos grasos saturados, tienden a
                            endurecerse a temperatura ambiente. La mantequilla, la manteca de
                            cochino y sebo de los animales, y los aceites de coco, palma y
                            semilla de palma, de las plantas son ejemplos comunes. Estas grasas
                            saturadas aumentan el colesterol sanguíneo, y deben ser evitadas.</SPAN></FONT>
                </FONT>
            </FONT>
        </FONT>
    </P>
    
    <P CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Times New Roman, serif">
            <FONT SIZE=2>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE"><I><B>Grasas
                                    Monosaturadas y Polinsaturadas</B></I></SPAN></FONT>
                </FONT>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE">.
                            Los aceites que permanecen líquidos a temperatura ambiente son altos
                            en grasas polinsaturadas. Ellos incluyen los aceites de cártamo,
                            soya, girasol, oliva y nabo. Todos son bajos en ácidos grasos
                            saturados, y pueden ser usados para ayudar a disminuir el colesterol
                            sanguíneo. Ellos tampoco contienen sodio.</SPAN></FONT>
                </FONT>
            </FONT>
        </FONT>
    </P>
    
    <P CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Times New Roman, serif">
            <FONT SIZE=2>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE"><I><B>Aceites
                                    Hidrogenados.</B></I></SPAN></FONT>
                </FONT>
                <FONT FACE="Courier New, monospace">
                    <FONT SIZE=3><SPAN LANG="es-VE">
                            Estos son encontrados ordinariamente en forma líquida a temperatura
                            ambiente, pero que han sido artificialmente endurecidos para producir
                            margarinas. Su efecto en el colesterol sanguíneo depende del grado
                            de hidrogenación. Es mejor buscar las margarinas que tienen los
                            aceites hidrogenados listados como segundo ingrediente.</SPAN></FONT>
                </FONT>
            </FONT>
        </FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Como
                una regla, mantenga su ingesta total de grasa en una cantidad no
                mayor del 30 por ciento de su dieta total, y use las siguientes
                listas para ayudar a identificar las grasas y aceites recomendados.</FONT>
        </FONT>
    </P>
    
    
