    <H2 LANG="es-VE" CLASS="western">Comida de mar</H2>
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 160%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Los
                restaurantes de comida de mar son lugares maravillosos para las
                personas con HTA. Todos ellos ofrecen pescados y mariscos asados, a
                la plancha, a la parrilla o al vapor, papas al vapor y vegetales como
                acompañantes, y una ensalada de lechuga. Evite el aderezo de la
                ensalada, el ketchup, la salsa tártara, y la salsa de rosada. En vez
                de estas, use el limón y el aceite y vinagre. Si la porción es muy
                grande, compártala con un amigo, o lleve algo para su casa no le de
                pena. . . . . . . </FONT>
        </FONT>
    </P>
     
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 160%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3><B>Momentos
                    especiales</B></FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western">Durante los momentos especiales, como
        los días feriados, haga selecciones cuidadosas y ahórrese algo de
        su ingesta diaria de sodio para su día feriado favorito.
    </P>
    <P LANG="es-VE" CLASS="western"><BR>
    </P>
    <P LANG="es-VE" CLASS="western">Cuando usted haya sido invitado a
        casa de un amigo, no le de pena de preguntar “¿Cual es el menú?”,
        y de compartir sus necesidades dietarias con su anfitrión. La
        mayoría de las veces habrá algunos alimentos que usted podrá
        disfrutar. O usted puede ajustar lo que usted come antes durante el
        día, para hacerle un espacio a las comidas con sodio que le serán
        servidas. No olvide, usted puede ofrecer llevar un plato que sea bajo
        en sodio y todos los demás podrán disfrutarlo también.
    </P>
    <P LANG="es-VE" CLASS="western"><BR>
    </P>
    <P LANG="es-VE" CLASS="western">Las bebidas antes de cenar, y el
        picar algo pueden ser un reto. Evite el queso, las carnes ahumadas, y
        el jamón. Siga los consejos de su médico acerca de las bebidas
        alcohólicas. Si le es permitida una pequeña cantidad, elija vino o
        cerveza, los cuales tienen menos alcohol que el licor. Las bebidas
        mezcladas y los vinos espumosos están bien si usted elige mezclas
        bajas en sodio. Las buenas elecciones son la mezcla de jugo de
        naranja y whisky sour, pina colada, daiquirí, aguaquina, ginger ale,
        y bebidas carbonadas simples. Evite el jugo de tomate, el jugo de
        vegetales y las bebidas servidas en vasos con sal previamente
        añadida. Pruebe algunas bebidas libres de alcohol, tales como los
        ponches, la mayoría de los lugares las ofrecen y son deliciosas.
    </P>
