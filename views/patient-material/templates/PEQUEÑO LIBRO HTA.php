    <H2 LANG="es-VE" CLASS="western"></H2>
    <H2 LANG="es-VE" CLASS="western">Tenga cuidado con ingerir mucha sal
        (sodio)</H2>
    <P LANG="es-VE" CLASS="western">Otra cosa importante que usted puede
        hacer es ingerir menos sodio. Comer mucho sodio causa retención de
        liquido en su cuerpo, y sube la tensión arterial.</P>
    <P LANG="es-VE" CLASS="western"> Eligiendo alimentos y bebidas con
        menos sodio, usted reduce la acumulación de agua en su cuerpo y la
        hipertensión. Esto disminuye la cantidad de trabajo que su corazón
        realiza y lo ayuda a sentirse mejor.
    </P>
     
