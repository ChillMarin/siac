        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>Parte
                de la diversión de salir a comer, es el probar la comida diferente
                de la que comemos en casa. Es por eso que comer variado es tan
                sabroso, recuerde en la variedad está el gusto por ejemplo El Chino,
