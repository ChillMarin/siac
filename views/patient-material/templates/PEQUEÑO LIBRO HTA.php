    <H2 LANG="es-VE" CLASS="western">Italiano</H2>
    <P LANG="es-VE" CLASS="western"
        STYLE="border-top: none; border-bottom: 3.00pt dotted #000000; border-left: none; border-right: none; padding-top: 0in; padding-bottom: 0.01in; padding-left: 0in; padding-right: 0in; line-height: 160%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>En los restaurantes
                italianos, comience con una ensalada mixta con aceite y vinagre, o
                una ensalada capresa sazonada con aceite de oliva y pimienta en
                granos, con hojas de albahaca al natural(aléjese de las aceitunas),
                o una alcachofa al vapor (sin mantequilla). Evite los platos tales
                como lasagna, raviolis y canelones –ellos son preparados
                previamente y altos en sodio. Las buenas elecciones son el marisco al
                vapor (use limón en vez de salsa de cocktail), o el pescado a la
                plancha; pasta con aceite de oliva y ajo, o vegetales asados. Pan
                Italiano o bruschetta (pan rebanado y enzima tomate picado y
                sazonado). Los helados italianos son buenas selecciones para el
                postre. </FONT>
        </FONT>
    </P>
     
    <H2 LANG="es-VE" CLASS="western">Mexicano</H2>
    <P LANG="es-VE" CLASS="western"
        STYLE="border-top: none; border-bottom: 3.00pt dotted #000000; border-left: none; border-right: none; padding-top: 0in; padding-bottom: 0.01in; padding-left: 0in; padding-right: 0in; line-height: 160%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>En los restaurantes
                Mexicanos, usted tiene que tener cuidado de los platos como los
                burritos, las enchiladas o los tacos. Pida la comida “sin queso”,
                y “sin sal”, tales como fajitas, preparadas con carne, pollo o
                pescado a la plancha, envueltas en tortillas al vapor. Añádales
                vegetales a la plancha o ensalada fresca. Coma las tortillas sin sal
                con salsa Mexicana. Sea cuidadoso con la crema agria. Los burritos
                rellenos de frutas son un sabroso final. </FONT>
        </FONT>
    </P>
    <P LANG="es-VE" CLASS="western"
        STYLE="border-top: none; border-bottom: 3.00pt dotted #000000; border-left: none; border-right: none; padding-top: 0in; padding-bottom: 0.01in; padding-left: 0in; padding-right: 0in; line-height: 160%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>La mayoría tiene
                carne, pollo, pescado y vegetales al vapor. Estos pueden ser
                recubiertos de crema agria, salsa de pato o de ciruela. Pida arroz
                hervido en vez de frito. Pida unos dados de vegetales al vapor,
                usualmente bajos en sal, en vez de las lumpias. Los sorbetes, la
                pina, las naranjas y galletas de la fortuna son buenos postres. </FONT>
        </FONT>
