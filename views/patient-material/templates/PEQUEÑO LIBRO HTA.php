    <H2 LANG="es-VE" CLASS="western">Siendo un sabio de la sal…</H2>
    <P LANG="es-VE" CLASS="western">Los aderezos de ensalada libres de
        grasa y bajos en sal, con frecuencia tienen mas sodio que las
        variedades regulares. Use solo aceite y vinagre.</P>
     
    <P LANG="es-VE" CLASS="western" STYLE="line-height: 160%">
        <FONT FACE="Courier New, monospace">
            <FONT SIZE=3>La
                pizza es la comida para llevar numero uno. La salsa de tomate, el
                peperoni, y el queso son altos en sodio. Los mejores toppings son el
                queso ricotta, los vegetales y el estilo bruschetta. Muchas pizzerias
                presentan “sin salsa, sin queso” o “prepáresela a su gusto”,
                como elecciones saludables.</FONT>
