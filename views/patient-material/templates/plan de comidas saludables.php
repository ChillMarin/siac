    <H2 LANG="es-VE" CLASS="western">
        <FONT SIZE=2>Bienvenidos
            al PLAN DE COMIDAS SALUDABLES</FONT>
        <FONT SIZE=2> </FONT><IMG SRC="<?php echo DIRECTORY . '/views/patient-material/templates/images/plan-comidas-saludables-1.gif' ?>"
