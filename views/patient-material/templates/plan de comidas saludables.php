    </H2>
    
    <P LANG="es-VE" CLASS="western" ALIGN=JUSTIFY>
        <FONT SIZE=2>El
            propósito de este plan es el de ayudar a alcanzar un mejor balance
            en la ingesta de grasas en su dieta. EL concepto de un balance de
            grasas proviene de una investigación conducida por científicos de
            universidades de los Estados Unidos, que lograron mejorar la relación
            del HDL colesterol “bueno” y LDL colesterol “malo”, en los
            pacientes. Para ello, se uso una mezcla balanceada de grasas como
            elemento clave de una dieta balanceada, siguiendo siempre los
            lineamientos de la dieta paso 1 de la Asociación Americana del
            Corazón, que limita el total de las grasas al 30% de las calorías
            consumidas; las grasas saturadas por debajo del 10%; y el colesterol
            dietario en menos de 300 mg por día.</FONT>
    </P>
    
    <P CLASS="western" ALIGN=JUSTIFY STYLE="line-height: 200%">
        <FONT FACE="Times New Roman, serif">
            <FONT SIZE=2>
                <FONT FACE="Courier New, monospace"><SPAN LANG="es-VE">Esta
                        dieta balanceada en grasas logra suministrar aproximadamente iguales
                        proporciones de los tres ácidos grasos principales:
                        poli-insaturados, monosaturados y saturados. </SPAN></FONT>
            </FONT>
        </FONT>
    </P>
    <H3 LANG="es-VE" CLASS="western" ALIGN=JUSTIFY></H3>
    <H3 LANG="es-VE" CLASS="western" ALIGN=JUSTIFY>
        <FONT SIZE=2>Más de
            la Dieta Balanceada en Grasas</FONT>
    </H3>
    
    <P LANG="es-VE" CLASS="western" ALIGN=JUSTIFY>
        <FONT SIZE=2>Además de
            las grasa balanceadas, es importante limitar las grasas saturadas al
            10% de las calorías, como lo hacemos en nuestro Plan de Comidas
            saludables. Para ello se deben evitar las comidas con aceites
            hidrogenados y ácidos grasos trans que se encuentran en los
            productos horneados, fritos, mantequillas regulares y margarinas. Es
            especialmente importante incluir un generoso aporte de frutas y
            vegetales, asi como de almendras y similares, así como ácidos
            grasos omega 3 que los encuentras en proporciones importantes en los
            pescados de piel azul ( sardinas, carite, jurel etc.). Nosotros
            incluimos cerca de 5 porciones por día en este plan. Existe una
            creciente evidencia de que los ácidos grasos Omega-3 pueden ser
            útiles en la disminución de del riesgo de enfermedades cardíacas.
            Es también una buena idea recibir ácidos grasos de cadena larga,en
            pescados de agua fría como el salmón, la trucha, etc. Sirva uno o
            dos platos de pescado por semana, como lo hacemos en nuestro Plan.</FONT>
    </P>
    
    <P LANG="es-VE" CLASS="western" ALIGN=JUSTIFY>
        <FONT SIZE=2>El
            ejercicio es muy importante. Consulte su médico. Sí camina por 30
            minutos al día, trota, nada, entrena con pesos ligeros o cualquier
            combinación de ejercicio regular, comiendo inteligentemente se
            comienza una buena salud.</FONT>
    </P>
    
