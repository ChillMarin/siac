        <FONT SIZE=2>1.
            Te levantarás y te acostarás todos los días a la misma hora.</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>Muchas
            veces la causa que de que no podamos dormir bien está en un
            desbarajuste de horarios. El sueño es rutinario, y le encantan los
            horarios estables. Eso de recuperar sueño los fines de semana puede
            resultar agradable, pero es causa de muchos desvelos.</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>2.Evita
            tomar sustancias excitantes a partir de media tarde.</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>No
            pienses solo en el café. También las bebidas de cola, la mayoría
            de los tés, el tabaco y el cacao pueden influir en tu sueño o hacer
            que te despiertes antes.</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>3.
            Quédate en la cama sólo lo necesario</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>Las
            horas que hay que dormir son las que tu cuerpo necesite para afrontar
            el día siguiente sintiéndote despejado. Todos no necesitamos dormir
            lo mismo. No permanezcas en la cama más de 8 horas al día, y sobre
            todo no veas la tele o trabajes en ella.</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>4.Evita
            el sedentarismo.</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>Hacer
            ejercicio de manera regular no sólo contribuye a que estés en forma
            y te hace sentir mejor, si no que ayuda a relajarse y descargar
            tensiones, lo cual puede ser de mucha ayuda cuando las preocupaciones
            nos quitan el sueño. Eso sí, tienes que dajer un buen rato entre
            las zapatillas y la cama: el ejercicio, siempre al menos un par de
            horas antes de dormir.</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>5.
            No abusarás de la siesta</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>Decían
            que la siesta era el deporte nacional y tan connatural a España como
            las sevillanas, los toros, la paella y el tinto de verano. Lo cierto
            es que la siesta diaria cada vez se practica menos, más por falta
            de tiempo que por falta de ganas. Siestas de unos minutos pueden ser
            beneficiosas, pero una siesta de más de 30 minutos puede hacer que
            por la noche nos cueste conciliar el sueño.</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>6.Tendrás
            tu habitación a punto para dormir</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>Necesitas
            un ambiente tranquilo y relajado. Cuidado con la luz excesiva, y
            intenta mantener una temperatura agradable. Ni frío ni calor. . .
            aunque es más fácil conciliar el sueño con unos grados de menos
            que de más. Aquí encontraréis buenos consejos sobre cómo elegir
            una habitación para dormir bien.</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>7.Desayuna
            como un rey, almuerza como un príncipe y cena como un mendigo</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">
        <FONT SIZE=2>Intenta
            mantener los horarios también en cuanto a las comidas. La cena no
            puede ser pesada, pero tampoco hay que irse a la cama con hambre. Hay
            algunos alimentos mejores que otros para conciliar el sueño, diseña
            una buena dieta para dormir mejor.</FONT>
    </P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">8.
        Sigue un ritual para dormir</P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">Convierte
        la preparación para irte a dormir en un ritual agradable: un baño
        caliente, algo de música para desconectar, ponerse el pijama,
        preparar qué te vas a poner mañana. . .</P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">9. Deja
        de consultar con la almohada</P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">Si
        tienes preocupaciones o tareas pendientes que no te dejan dormir,
        apúntalas en un cuaderno. Listarlas te ayudará a no darle
        demasiadas vueltas a la cabeza en la cama.</P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">10. Si
        no tienes sueño, levántate</P>
    <P LANG="es-VE" CLASS="western" STYLE="margin-bottom: 0.14in">No te
        quedes en la cama más de media hora mirando a la pared. Si pasado
        este tiempo no has logrado dormirte, es más fácil que vuelvas a
        conciliar el sueño si te levantas y te relajas un rato que si te
        fuerzas a permanecer en la cama sin sueño. Vuelve sólo cuando
        sientas somnolencia</P>
