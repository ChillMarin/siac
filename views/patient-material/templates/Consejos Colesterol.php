<P LANG="es-ES" CLASS="western" ALIGN=CENTER><FONT FACE="Comic Sans MS, cursive"><FONT SIZE=5>El
Colesterol  y los triglicéridos</FONT></FONT></P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><FONT FACE="Arial, sans-serif">El
Colesterol y los triglicéridos  son  sustancias grasas presente en
todas las personas y constituye parte fundamental de nuestro
organismo. </FONT>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><FONT SIZE=2><FONT FACE="Arial, sans-serif">Son
 es indispensable para nuestra vida y normalmente, se halla
circulando en la sangre en pequeñas cantidades, suficientes para el
buen funcionamiento del cuerpo.</FONT></FONT></P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY> <FONT SIZE=2><FONT FACE="Arial, sans-serif">Son
 es producido principalmente por nuestro propio cuerpo, aunque una
pequeña cantidad proviene de los alimentos que ingerimos.</FONT></FONT></P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><FONT FACE="Arial, sans-serif"><FONT SIZE=2>El
Colesterol viaja en la sangre de diferentes formas, que son como los
miembros de una misma   familia; así, hay una forma de  Colesterol 
llamada  Colesterol-LDL </FONT></FONT>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><FONT FACE="Arial, sans-serif"><FONT SIZE=2>o
malo, el cual puede producir daño a nuestras arterias; y existe una
forma de Colesterol llamado Colesterol-HDL o colesterol bueno, que
reduce el peligro del Colesterol malo.</FONT></FONT></P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><FONT FACE="Arial, sans-serif"><FONT SIZE=2>El
Colesterol malo, o Colesterol-LDL, se convierte en un riesgo para
nuestro cuerpo cuando se encuentra circulando en la sangre en
cantidades aumentadas. Esta sustancia es capaz de penetrar al
interior de nuestras arterias y acumularse allí para producir una
placa de grasa que inflama y obstruye la arteria. Este daño, que
ocurre de manera silente a lo largo de varios años, se llama
enfermedad aterosclerótica y es la responsable de los ataques al
corazón y la mayoría de los infartos del cerebro.</FONT></FONT></P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><FONT SIZE=2><FONT FACE="Arial, sans-serif">Algunas
personas producen gran cantidad de  triglicéridos  en su cuerpo, lo
cual las hace ser más propensas de sufrir la enfermedad
aterosclerótica. Lo mismo ocurre con aquellas personas que consumen
gran cantidad de alimentos ricos en grasas.</FONT></FONT></P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
</P>
<P LANG="es-ES" ALIGN=JUSTIFY><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 11pt"><FONT SIZE=2>Para
conocer cuánto Colesterol  y triglicéridos tenemos, y si se
encuentra en nivel de peligro, basta con tomarnos una simple muestra
de sangre. </FONT></FONT></FONT>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><FONT FACE="Arial, sans-serif"><FONT SIZE=2>Todas
las personas, desde su adolescencia, deberían ser examinadas
regularmente para conocer su nivel de Colesterol y si este se
encuentra elevado, deberían recibir tratamiento. </FONT></FONT>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><BR>
</P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY><FONT FACE="Arial, sans-serif"><FONT SIZE=2>Si
el nivel de Colesterol total o de Colesterol malo de una persona se
encuentra por encima de esos límites, se debe consultar al médico,
quien indicará una serie de medidas a seguir entre las cuales se
cuentan: realizar ejercicios de manera regular y</FONT></FONT></P>
<P LANG="es-ES" CLASS="western" ALIGN=JUSTIFY> <FONT SIZE=2><FONT FACE="Arial, sans-serif"><U><B>disminuir
la ingesta de alimentos ricos en grasas tales como:</B></U></FONT></FONT></P>
<P LANG="es-ES" ALIGN=JUSTIFY> <FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 11pt"><I><B>las
carnes de ganado  rojas con grasa. La carne de cochino, la manteca ,
los sesos, hígado y riñones y demás vísceras, los embutidos, la
tocineta y el chicharrón, la yema del huevo, la leche completa y sus
derivados y las comidas frita, la mantequilla, el queso amarillo,
aceite de ajonjolí.</B></I></FONT></FONT></P>
<P LANG="es-ES" ALIGN=JUSTIFY><BR>
</P>
<P LANG="es-ES" ALIGN=JUSTIFY STYLE="font-style: normal"><FONT FACE="Arial, sans-serif"><FONT SIZE=2 STYLE="font-size: 11pt"><B>Utilice
carnes de pescado o pollo no fritas, ,margarina, leche descremada,
quesos blancos con poca sal, Claras de huevo, frutas, verduras,
cereales  integrales, aceite de maíz o de girasol , de oliva   o  de
