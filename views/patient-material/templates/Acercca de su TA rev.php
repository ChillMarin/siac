<H3 LANG="es-VE" CLASS="western">Medicación</H3>
<P LANG="es-VE" CLASS="western">Algunas personas necesitan medicación
para ayudarse a reducir la presión arterial alta. Existen muchos
medicamentos disponibles para esto. Algunos eliminan el exceso de
líquidos y la sal. Otros abren los vasos sanguíneos estrechados.
Otros previenen la constricción y estrechamiento de los vasos
sanguíneos. 
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P CLASS="western" STYLE="line-height: 200%"><FONT FACE="Times New Roman, serif"><FONT SIZE=2><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE">Los
medicamentos disminuyen la presión arterial alta en la mayoría de
