su doctor le encuentre un medicamento más apropiado para usted, </SPAN></FONT></FONT><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE"><B>lo
que si es importante destacar es que si usted tiene Hipertensión
arterial muy probablemente deberá tomar su medicación de por vida.</B></SPAN></FONT></FONT></FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Conozca
