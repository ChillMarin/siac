<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3><B>¿Cómo
se puede saber si se tiene la tensión alta?</B></FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>La
presión arterial alta usualmente no da síntomas. De hecho, muchas
personas la tienen por años sin saberlo. Por eso que es tan
peligrosa.</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>La
única manera de saber que usted tiene esta enfermedad es midiéndola
por  Su doctor, u otro profesional de la salud. </FONT></FONT>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3><B>¿cómo
daña la Hipertensión arterial al organismo?</B></FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>La
Hipertensión arterial puede dañar su cuerpo de muchas maneras.
Principalmente porque le añade trabajo a su corazón y a las
arterias. Debido a que su corazón tiene que trabajar más fuerte que
lo normal por más tiempo, tienen tendencia a ser más grande. Un
corazón discretamente más grande puede trabajar bien, pero sí es
muy grande, puede tener dificultades para llenar las demandas del
cuerpo.</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>A
medida que usted envejece, sus arterias se endurecen y se hacen menos
elásticas. Esto ocurre gradualmente en todas las personas,
independientemente de que tengan la presión arterial elevada o no.
Pero el tener la Hipertensión arterial alta este proceso se acelera.</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>La
presión arterial elevada aumenta el riesgo de accidentes cerebro
vasculares y de dañar sus riñones y ojos. comparados con las
personas que tienen la presión arterial normal, las personas con la
presión arterial alta tienen :</FONT></FONT></P>
<UL>
	<LI><P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Siete
	veces más probabilidades de tener un accidente cerebrovascular.</FONT></FONT></P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<UL>
	<LI><P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Tres
	veces más probabilidades de desarrollar enfermedad arterial
	coronaria.</FONT></FONT></P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="margin-left: 0.31in; line-height: 200%">
<BR>
</P>
<UL>
	<LI><P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Seis
	veces más probabilidades de desarrollar insuficiencia Cardíaca.</FONT></FONT></P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<UL>
	<LI><P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Siete
	veces más probabilidades de desarrollar insuficiencia renal.</FONT></FONT></P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Si
usted tiene Hipertensión arterial, sigua los consejos de su médico
coma con menos sal, deje de fumar si fuma, disminuya el consumo de
alcohol, asista a sus consultas y tómese sus medicamentos. La
Hipertensión arterial, en la gran mayoría de los pacientes  no se
cura, pero usualmente puede ser controladas. Y sus efectos pueden ser
prevenidos o reducidos  si es controlada tempranamente y mantenida
bajo control de por vida.  </FONT></FONT>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P CLASS="western" STYLE="line-height: 200%"><FONT FACE="Times New Roman, serif"><FONT SIZE=2><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE"><B>¿Qué
puedo hacer con respecto la presión arterial alta?</B></SPAN></FONT></FONT><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE">
    </SPAN></FONT></FONT></FONT></FONT>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>El
tratamiento de la Hipertensión arterial se basa en la combinación
de dieta, ejercicio y medicación. </FONT></FONT>
</P>
<H3 LANG="es-VE" CLASS="western">Dieta</H3>
<P LANG="es-VE" CLASS="western">Muchas personas con presión arterial
alta tienen sobrepeso. Si este es su caso, su médico puede
prescribirle una dieta. Otras personas profesionales de la salud
calificadas (nutricionistas, enfermeras, asistentes de médicos,
etc.) pueden guiarlo en comenzar, o en como seguir una dieta.   
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>A
veces, cuando las personas pierden peso, su presión arterial
disminuye automáticamente. El hecho de Tener sobrepeso es un factor
de riesgo para la enfermedad cardíaca. Perder peso ayudará a su
presión arterial, y también a mantenerse saludable en otros
aspectos.   </FONT></FONT>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Si
usted recibe una dieta, sígala estrictamente, </FONT></FONT>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>.
Esto significa que usted deberá :</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<UL>
	<LI><P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Evitae
	las comidas saladas.</FONT></FONT></P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>A
veces el comer menos sodio ayuda a bajar la presión arterial. Para
comer  con menos sodio usted debe:</FONT></FONT></P>
<UL>
	<LI><P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Disminuir
	la cantidad de sal que usted usa al cocinar, y en la mesa.</FONT></FONT></P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<UL>
	<LI><P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Comenzar
	a leer las etiquetas de los empacados para conocer el contenido de
	sodio de los preparados alimentarios.</FONT></FONT></P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<UL>
	<LI><P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Evitar
	el uso de enlatados y de cubitos.</FONT></FONT></P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<UL>
	<LI><P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Evitar
	el uso de sopas de sobre.</FONT></FONT></P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<UL>
	<LI><P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Comer
	menos embutidos y quesos salados.</FONT></FONT></P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">
<FONT FACE="Courier New, monospace"><FONT SIZE=3>Experimente con
hierbas y especias como aderezos, usted puede disfrutar de una
comidas gustosa, pero sin sal si utiliza para sazonar las comidas
productos naturales como el ajo, el cilantro, el pimentón, la
páprika, la cebolla, la mostaza etc.</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>No
haga mayores cambios en su dieta sin primero obtener una adecuada
orientación médica. Y una vez que se le ha prescrito una dieta,
manténgase en ella.</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
