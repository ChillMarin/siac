<P LANG="es-VE" CLASS="western">La presión arterial alta es una
enfermedad de por vida. Puede ser controlada pero no curada. Una vez
que usted comience a manejarlo y comenzar un programa de tratamiento,
será sencillo mantener la presión arterial más baja. Al controlar
su presión arterial alta, usted disminuirá su riesgo de
enfermedades como accidentes cerebrovasculares, ataque al corazón,
insuficiencia cardíaca y enfermedad renal. 
</P>
