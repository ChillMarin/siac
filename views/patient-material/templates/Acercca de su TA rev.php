	</P>
</UL>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<UL>
	<LI><P CLASS="western" STYLE="line-height: 200%"><FONT FACE="Times New Roman, serif"><FONT SIZE=2><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE"><B>Sedentarismo
	</B></SPAN></FONT></FONT><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE">–
	Una vida con poca  actividad física es decir  sedentaria 
