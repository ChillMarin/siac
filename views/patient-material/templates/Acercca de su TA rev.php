<H3 LANG="es-VE" CLASS="western"></H3>
<H3 LANG="es-VE" CLASS="western">Ejercicio y Recreación</H3>
<P LANG="es-VE" CLASS="western">No tenga temor de estar activo. La
actividad física debe ser definitivamente parte de su programa
diario. Inclusive lo puede ayudar a perder peso, o a mantener su peso
ideal. 
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Su
