<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3><B>¿Por
qué es importante la presión arterial?</B></FONT></FONT></P>
<P LANG="es-VE" CLASS="western">Todos necesitamos, la presión
arterial. Sin ella, la sangre no podría circular adecuadamente  a
través del cuerpo, y no le llegaría a los distintos órganos,  y
entonces los órganos no tendrían  él oxigeno y nutrientes
necesarios para  vivir y trabajar adecuadamente.</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Por
eso es importante que usted conozca sobre su presión arterial – y
aprenda a  como mantenerla en un nivel  apropiado.</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>El
funcionamiento adecuado del corazón es muy importante para mantener
la presión en rangos normales, cuando su corazón late en cada
latido  bombea sangre a sus arterias y crea presión dentro de ellas.
Esta presión (presión arterial) lleva su sangre es la fuerza que
hace  fluir la sangre a todas las partes de su cuerpo. </FONT></FONT>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Si
usted es una persona saludable sus arterias, que  son unos conductos
musculares y con propiedades elásticas, se estirarán cada vez que
su corazón bombee sangre a través de ellas. EL grado de
estiramiento depende de cuanta fuerza ejerza la sangre.</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>En
condiciones normales su corazón late aproximadamente entre   60 a 80
veces en un minuto. Su presión arterial aumenta durante cada latido 
y disminuye cuando el corazón se relaja entre los latidos.</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"> <FONT FACE="Courier New, monospace"><FONT SIZE=3>La
presión arterial normalmente  cambia de minuto en minuto, y se
altera hacia arriba o hacia abajo en respuesta a muchos estímulos
pej: cambios en la postura, ejercicios, después de comer, cuando nos
emocionamos, cuando nos alegramos, cuando nos preocupamos, durante el
sueño etc..</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3><B>¿Cómo
se mide la Presión Arterial y qué  es presión arterial elevada?</B></FONT></FONT></P>
<P LANG="es-VE" CLASS="western">Al medir la presión arterial esta se
reporta con dos números, por ejemplo  como 124/82 mmHg (milímetros
de mercurio. El número mayor conocido como presión sistólica mide
la presión de sus arterias cuando el corazón bombea la sangre hacia
las arterias en cada latido. El número menor conocido como presión
diastólica mide la presión mientras el corazón está relajado 
entre cada latido.</P>
<P CLASS="western" STYLE="line-height: 200%"><FONT FACE="Times New Roman, serif"><FONT SIZE=2><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE">La
presión arterial normal se encuentra dentro de un rango no es un
número fijo, si usted es un adulto debe ser mayor de </SPAN></FONT></FONT><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE"><B>90/60
mm</B></SPAN></FONT></FONT><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE">Hg
y  menor a </SPAN></FONT></FONT><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE"><B>140/90</B></SPAN></FONT></FONT><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE">
mmHg.</SPAN></FONT></FONT></FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3>Si
usted tiene diabetes, insuficiencia renal  o insuficiencia cardíaca,
su presión nunca debe ser mayor  de 130/85 mmHg. Si su presión
arterial se encuentra por encima  de ese umbral en forma habitual,
usted es hipertenso.</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
