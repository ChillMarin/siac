<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><FONT FACE="Courier New, monospace"><FONT SIZE=3><B>¿Porqué
se produce la presión  alta?</B></FONT></FONT></P>
<P LANG="es-VE" CLASS="western">La presión arterial alta llamada
hipertensión arterial, no debe confundirse con tensión nerviosa, y
generalmente no da ningún síntoma, no produce en la mayoría de los
pacientes ninguna manifestación ni dolor de cabeza, ni mareo etc.</P>
<P CLASS="western" STYLE="line-height: 200%"><FONT FACE="Times New Roman, serif"><FONT SIZE=2><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE">De
hecho, usted puede tener presión arterial muy alta y no saberlo. </SPAN></FONT></FONT><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE"><B>Por
eso es llamada el:</B></SPAN></FONT></FONT></FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%">“<FONT FACE="Courier New, monospace"><FONT SIZE=4 STYLE="font-size: 16pt"><B>Asesino
Silencioso”.</B></FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<P LANG="es-VE" CLASS="western">Cerca del 90-95% de los casos de la
presión arterial alta no tiene causas conocidas. Sin embargo,
algunos factores aumentan las posibilidades  de desarrollarla, Estos
factores son llamados factores de riesgo.</P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
</P>
<H2 LANG="es-VE" CLASS="western" STYLE="text-decoration: none">Factores
de riesgo que usted puede modificar:</H2>
<UL>
	<LI><P CLASS="western" STYLE="line-height: 200%"><FONT FACE="Times New Roman, serif"><FONT SIZE=2><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE"><B>Obesidad</B></SPAN></FONT></FONT><FONT FACE="Courier New, monospace"><FONT SIZE=3><SPAN LANG="es-VE">
	– Las personas con un índice de masa corporal(IMC) que es una
