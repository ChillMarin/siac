Cualquier actividad física que usted disfrute y haga regularmente,
probablemente será correcta, debería hacer al menos una hora de
ejercicio por lo menos cinco veces a la semana.</FONT></FONT></P>
<P LANG="es-VE" CLASS="western" STYLE="line-height: 200%"><BR>
