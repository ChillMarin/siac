            que la cifra de mujeres mayores y mujeres al riesgo en la población
            están creciendo, el diagnostico y tratamiento de enfermedad
            cardiaca, accidente cerebrovascular, y otras enfermedades
            cardiovasculares es vital. Los médicos deben tomar acción para
            prevenir estas enfermedades, antes de los signos y síntomas
            aparezcan, o de que ocurra un ataque cardiaco o un accidente
            cerebrovascular. La prevención y el control de los factores de
            riesgo debe comenzar cuando una mujer es joven y continuar a lo largo
            de la vida. </FONT>
