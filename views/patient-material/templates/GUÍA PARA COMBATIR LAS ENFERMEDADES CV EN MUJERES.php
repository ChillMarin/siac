    </P>
    <P LANG="es-VE">
        <FONT SIZE=3>¿SABE USTED QUE LA ENFERMEDAD CARDIACA
            Y EL ACCIDENTE CEREBROVASCULAR SON ASESINOS CON “IGUAL
            OPORTUNIDAD”?</FONT>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>La
                    enfermedad cardiaca y el accidente cerebrovascular matan a más de
                    medio millón de mujeres en los Estados Unidos cada año – más
                    que las 16 causas siguientes de muerte combinadas, y cerca del doble
                    de tanto como todas las formas de cáncer, incluyendo el cáncer de
                    mama.</FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE">
                <FONT SIZE=3><SPAN STYLE="font-weight: normal">Las
                        mujeres que tienen ataques cardiacos tienen </SPAN>más probabilidad
                    que los hombres de morir<SPAN STYLE="font-weight: normal"> de esto
                        en unas pocas semanas.</SPAN></FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE">
                <FONT SIZE=3><SPAN STYLE="font-weight: normal">La
                        enfermedad cardiaca coronaria causo las muertes de más de 236.000
                        de mujeres en 1995 – </SPAN>49% de todas las muertes por
                    enfermedad cardiaca coronaria<SPAN STYLE="font-weight: normal">.</SPAN></FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE">
                <FONT SIZE=3><SPAN STYLE="font-weight: normal">Mas
                        de 96.000 mujeres murieron por accidente cerebrovascular en 1995
                        –</SPAN>61% de todas las muertes por accidente cerebrovascular<SPAN
                        STYLE="font-weight: normal">.
                    </SPAN></FONT>
            </P>
    </UL>
