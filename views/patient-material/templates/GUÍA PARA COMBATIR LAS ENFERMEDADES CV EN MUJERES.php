                estar orientada en la familia, incluyendo una revisión de la
                historia familiar. Los hábitos de vida saludables y la intervención
                temprana para controlar los factores de riesgo tratables, ayudaran a
                mitigar la severidad de los factores de riesgo intratables que
                resultan de la edad y la genética. </FONT>
        </FONT>
