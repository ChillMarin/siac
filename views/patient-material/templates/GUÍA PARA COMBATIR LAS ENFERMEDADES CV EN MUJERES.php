                    investigación cardiovascular fueron realizados en hombres<SPAN STYLE="font-weight: normal">.
                        Los resultados de los estudios clínicos que se están realizando
                        actualmente, pueden ayudar a clarificar las diferencias del sexo que
                        afectan la fisiopatología, el diagnostico y el tratamiento de las
                        mujeres con enfermedad cardiaca.</SPAN></FONT>
