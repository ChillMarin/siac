    </P>
    <P LANG="es-VE">
        <FONT SIZE=3><SPAN STYLE="font-weight: normal">El
                ataque cardiaco y el accidente cerebrovascular pueden aparecer a
                cualquier edad, pero el riesgo incrementa paulatinamente con la edad.
                Cada año los eventos coronarios matan casi 20.000 mujeres </SPAN>menores
            de 65 años<SPAN STYLE="font-weight: normal"> – más del 31% de
                estas son menores de 55 años. Y más de una cuarta parte de las
                mujeres que tienen un accidente cerebrovascular en una edad
                determinada son menores de 65 años. </SPAN></FONT>
    </P>
