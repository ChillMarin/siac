        <FONT SIZE=3>¿LOS MÉDICOS LE DAN UNA “OPORTUNIDAD
            IGUAL” DE CUIDADO MEDICO A SUS PACIENTES?</FONT>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal">
        <FONT SIZE=3>Aunque la
            situación esta mejorando, las investigaciones sugieren que menos
            mujeres que hombres con sospecha de síntomas agudos de un ataque
            cardiaco son referidas para pruebas no invasivas, y menos mujeres que
            hombres que tienen pruebas positivas para enfermedad cardiaca son
            recomendadas para pruebas adicionales y tratamiento. Debido a la alta
            tasa de fatalidad asociada con el primer ataque cardiaco en mujeres,
            es importante evaluar a las mujeres con sospecha de ataques cardiacos
            con prontitud, cuidadosamente y completamente. Es aun más importante
            enfatizar la prevención a través de la reducción de los riesgos. </FONT>
    </P>
