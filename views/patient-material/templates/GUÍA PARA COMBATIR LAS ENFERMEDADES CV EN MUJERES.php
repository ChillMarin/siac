            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal">
        <FONT SIZE=3>Mida la
            lipoproteina de baja densidad (LDL) si los niveles de colesterol de
            la paciente están como sigue:</FONT>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>Colesterol
                    total de 240 mg/dl o más</FONT>
            </P>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>Colesterol
                    total de 240 mg/dl o más, con 2 o más factores de riesgo.</FONT>
            </P>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>Lipoproteina
                    de alta densidad (HDL) menos de 35 mg/dl.</FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal">
        <FONT SIZE=3>La meta
            primaria es un LDL menor de 160 mg/dl si la paciente no tiene más de
            1 factor de riesgo, o LDL menor de 130 mg/dl, si ella tiene 2 o más
            factores de riesgo. Las metas secundarias para la población general
            son un HDL mayor de 35 mg/dl y triglicéridos menores de 200 mg/dl.
            Muchos médicos sienten que un HDL de más de 45 mg/dl es más
            apropiado para las mujeres.</FONT>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal">
        <FONT SIZE=3>En las
            pacientes cuyo LDL exceda estas metas, comience el la Dieta del Paso
            II del AHA - no más de 30% de calorías como grasa, menos del 7% de
            las calorías como grasa saturada, y menos de 200 mg/dl de colesterol
            dietario – así como también control de peso. Considere añadir la
            terapia de drogas en pacientes con niveles elevados de LDL colesterol
            que persistan a pesar de la Dieta del Paso II. </FONT>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>Actividad
                    Física. Pregunte acerca de hábitos de ejercicios en los exámenes
                    de rutina. Estimule 30 minutos de actividad de intensidad moderada
                    la mayor parte de los días de la semana (caminar, ciclismo, trotar,
                    y otras actividades aerobicas), así como también el incremento de
                    las actividades diarias en las personas inactivas. El ejercicio
                    regular puede mejorar el acondicionamiento y promover una figura
                    optima. Aconseje programas médicamente supervisados para aquellas
                    con capacidad funcional baja y/u otros problemas de salud. </FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE">
                <FONT SIZE=3><I>Manejo del peso</I><SPAN STYLE="font-weight: normal">.
                        Determine el peso y la talla de sus pacientes, el índice de masa
                        corporal (BMI), y la circunferencia de la cintura, como parte de los
                        exámenes de rutina. La meta es alcanzar y mantener un peso ideal,
                        definido como un rango de BMI de 21-25 Kg/m2 (Un BMI de 25 Kg/m2
                        corresponde a un percentil 110 del peso ideal). Una circunferencia
                        de la cintura deseable para las mujeres es aquella no mayor de 88 cm
                        o 35 pulgadas. Comience un manejo del peso y de la actividad física
                        como sea apropiado. </SPAN></FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE">
                <FONT SIZE=3><I>Estrógenos. </I><SPAN STYLE="font-weight: normal">Considere
                        la terapia de reemplazo de estrógenos (ETR o HTR) en las mujeres
                        postmenopáusicas, especialmente aquellas con múltiples factores de
                        riesgo coronarios, tales como elevado LDL colesterol. Individualice
                        su recomendación consistentemente con los otros factores de riesgo
                        de la paciente, u otros problemas de salud, y con los riesgos y los
                        beneficios de la ERT y HTR. En mujeres más jóvenes, aconséjeles
                        sobre el riesgo de tomar píldoras anticonceptivas si son fumadoras.
                    </SPAN></FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <P LANG="es-VE">
        <FONT SIZE=3>Beneficios de la ERT y HRT</FONT>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>Muchos
                    estudios muestran un riesgo consistentemente bajo de enfermedad
                    cardiaca en tanto como un 50% en las mujeres que toman estrógenos
                    después de la menopausia, comparado con las mujeres que no toman
                    estrógenos. Los mismos estudios muestran un riesgo menos
                    consistentemente bajo de accidente cerebrovascular. Sin embargo,
                    otras razones además de los estrógenos pueden explicar algo de la
                    protección, tales como la educación, mejor acceso a cuidados de
                    salud, y menores factores de riesgo entre las mujeres que toman
                    estrógenos. </FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>La
                    terapia con estrógenos tiene efectos positivos en varios factores
                    de riesgo de enfermedad cardiaca. El Estudio de Intervención con
                    Progestina/Estrógenos en Mujeres Postmenopáusicas encontró que la
                    ERT y HRT producen incremento del HDL. (Aun pequeños incrementos
                    del HDL pueden potencialmente bajar el riesgo de la enfermedad
                    cardiaca). También se encontró que la HRT disminuye el LDL y el
                    fibrinógeno, un factor de coagulación sanguínea que es un
                    predictor del ataque cardiaco y del accidente cerebrovascular. </FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>Otras
                    investigaciones han mostrado que la terapia con estrógenos ayuda a
                    los vasos sanguíneos a relajarse y responder al ejercicio y al
                    estrés, manteniendo las arterias reactivas e incrementando el flujo
                    sanguíneo. </FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>La ERT
                    y HRT ayudan a aliviar los síntomas menopáusicos, protegen contra
                    la osteoporosis y ayudan a prevenir fracturas. </FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>Datos
                    clínicos tempranos han mostrado que la perdida de estrógenos en
                    las mujeres puede estar ligada al riesgo de enfermedad de Alzheimer.
                    La enfermedad puede ser menos probable de aparecer en mujeres que
                    toman estrógenos después de la menopausia. </FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>La ERT
                    (estrógenos solos) incrementa el riesgo de cáncer endometrial y
                    uterino en mujeres con un útero intacto. Sin embargo, la HRT
                    (estrógenos y progestina combinados) reduce el riesgo de cáncer
                    endometrial al normal.</FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>La ERT
                    y la HRT pueden incrementar los niveles de triglicéridos en las
                    mujeres, pero la significancia clínica de esto se desconoce. </FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>Algunas
                    investigaciones sugieren un riesgo incrementado de coágulos
                    sanguíneos en las mujeres tomando ERT o HRT, aunque esto es raro. </FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <UL>
        <LI>
            <P LANG="es-VE" STYLE="font-weight: normal">
                <FONT SIZE=3>Las
                    mujeres del Estudio de Salud de las Enfermeras, y en varios otros
                    estudios que usaron hormonas por 10 años o más, tuvieron un
                    incremento del cáncer de mama. </FONT>
            </P>
    </UL>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal">
        <FONT SIZE=3>Nosotros
            debemos saber más acerca de los beneficios y los riesgos del uso de
            las hormonas, a partir de los resultados de un estudio grande,
            doble-ciego, randomizado, controlado con placebo, el cual se esta
            realizando actualmente. El Estudio de Iniciativa de Salud en las
            Mujeres (WHI), se esta llevando a cabo en el Instituto Nacional de
            Salud, y esta planeado para concluir en el año 2006. El WHI es el
            primer estudio que proveerá datos de enfermedades en una minoría
            étnica de mujeres recibiendo terapia hormonal. </FONT>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal">
        <FONT SIZE=3>Hay estudios
            que han mostrado que solo un tercio de los pacientes elegibles
            continúan con intervenciones para los factores de riesgo a largo
            plazo. Sin embargo, los datos también muestran que esta proporción
            puede aumentar significativamente por una estrategia de equipo.
            Cuando los profesionales de salud - incluyendo a los médicos,
            enfermeras, nutricionistas, otros clínicos y educadores de la salud-
            manejan la terapia de reducción de riesgo con métodos de
            seguimiento tales como visitas a la clínica, y contacto telefónico,
            la cooperación de los pacientes es estimulada. </FONT>
    </P>
    <P LANG="es-VE" STYLE="font-weight: normal"><BR>
    </P>
